open Lwt.Syntax
open Belenios_core.Common
open Belenios_tool_js_common.Tool_js_html
open Common

let show main uuid =
  let@ () = show_in main in
  let* x = get (fun x -> x) "elections/%s" uuid in
  let@ raw_election = with_ok "election" x in
  Lwt.return @@ [txt raw_election]
